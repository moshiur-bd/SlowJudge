#include<stdio.h>
int main () {
    int n,i,j,k,day[30],count=0;
    while(scanf ("%d",&n)==1) {
        while(scanf ("%d",&k)==1);
        {
            scanf ("%d",&day[j]);

            if (day[j]==0)
                count++;
        }
        if (count<k)
            printf (":D\n");
        else {
            printf (":\(\n");
        }
    }

}
